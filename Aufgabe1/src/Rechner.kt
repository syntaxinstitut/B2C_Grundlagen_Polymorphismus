class Rechner {
    fun add(a: Int, b: Int): Int {
        return a + b
    }

    fun add(a: Double, b: Double): Double {
        return a + b
    }

    fun subtrahieren(a: Int, b: Int): Int {
        return a - b
    }

    fun subtrahieren(a: Double, b: Double): Double {
        return a - b
    }

    fun multiplizieren(a: Int, b: Int): Int {
        return a * b
    }

    fun multiplizieren(a: Double, b: Double): Double {
        return a * b
    }

    fun dividieren(a: Int, b: Int): Int {
        return a / b
    }

    fun dividieren(a: Double, b: Double): Double {
        return a / b
    }
}